"""Kernel registry: maps name -> metadata. One entry per kernel.

Each MLX baseline file now contains ONLY the Model class. Shape config lives
here. get_inputs, make_inputs, reference, scalars, grid, flops, bytes are all
auto-generated by mlx_helpers.load_baseline().

To add a kernel:
  1. Create mlx/kernels/common/<name>.py with just the Model class
  2. Add an entry to REGISTRY below
  3. Create src/kernels/common/<name>.metal
"""

# (shape) -> (grid_x, grid_y, grid_z)
def _grid_1d(n_el, tg=1024, grid_x=64):
    return (grid_x * tg, 1, 1)

def _grid_2d(rows, cols, tg=1024):
    return (cols, rows, 1)

REGISTRY = {}

# -- Helpers that auto-generate metadata from a simple spec -------------------

def ew(name, metal_func, shape=(16, 16384), grid_size=64 * 1024, flops_mul=5,
       rtol=1e-3, atol=1e-3, extra_scalars=None):
    """Element-wise kernel."""
    n_el = shape[0] * shape[1]
    REGISTRY[name] = dict(
        metal_function=metal_func,
        threadgroup=(1024, 1, 1),
        input_bindings=(0,),
        input_shapes=[shape],
        output_shape=shape,
        rtol=rtol, atol=atol,
        grid=(grid_size, 1, 1),
        scalars=[dict(binding=2, dtype="u32", value=n_el),
                 dict(binding=3, dtype="u32", value=grid_size)] +
                (extra_scalars or []),
        flops=n_el * flops_mul,
        bytes=2 * n_el * 4,
    )

def matmul(name, metal_func, M, N, K, BM=64, BN=64, BK=16, TG=256,
           rtol=1e-3, atol=1e-3):
    """Matrix multiply kernel."""
    REGISTRY[name] = dict(
        metal_function=metal_func,
        threadgroup=(TG, 1, 1),
        input_bindings=(0, 1),
        input_shapes=[(M, K), (K, N)],
        output_shape=(M, N),
        rtol=rtol, atol=atol,
        grid=((N // BN) * TG, M // BM, 1),
        scalars=[dict(binding=3, dtype="u32", value=M),
                 dict(binding=4, dtype="u32", value=N),
                 dict(binding=5, dtype="u32", value=K)],
        flops=2 * M * N * K,
        bytes=4 * (M * K + K * N + M * N),
    )

def batched_matmul(name, metal_func, B, M, N, K, BM=64, BN=64, BK=16, TG=256,
                   rtol=1e-3, atol=1e-3):
    """Batched matrix multiply kernel."""
    REGISTRY[name] = dict(
        metal_function=metal_func,
        threadgroup=(TG, 1, 1),
        input_bindings=(0, 1),
        input_shapes=[(B, M, K), (B, K, N)],
        output_shape=(B, M, N),
        rtol=rtol, atol=atol,
        grid=((N // BN) * TG, (M // BM) * B, 1),
        scalars=[dict(binding=3, dtype="u32", value=M),
                 dict(binding=4, dtype="u32", value=N),
                 dict(binding=5, dtype="u32", value=K)],
        flops=B * 2 * M * N * K,
        bytes=B * 4 * (M * K + K * N + M * N),
    )

def reduction(name, metal_func, rows, cols, tg=1024, rtol=1e-3, atol=1e-3,
              flops=0, bytes=0, extra_scalars=None, output_shape=None,
              input_shape=None):
    """Reduction kernel (one threadgroup per row)."""
    out = output_shape or (rows, cols)
    inp = input_shape or (rows, cols)
    REGISTRY[name] = dict(
        metal_function=metal_func,
        threadgroup=(tg, 1, 1),
        input_bindings=(0,),
        input_shapes=[inp],
        output_shape=out,
        rtol=rtol, atol=atol,
        grid=(rows, cols, 1),
        scalars=[dict(binding=2, dtype="u32", value=cols)] +
                (extra_scalars or []),
        flops=flops or rows * cols * 5,
        bytes=bytes or 2 * rows * cols * 4,
    )

def scan(name, metal_func, rows, cols, tg=1024, rtol=1e-3, atol=1e-3,
         flops_mul=1):
    """Parallel scan kernel."""
    REGISTRY[name] = dict(
        metal_function=metal_func,
        threadgroup=(tg, 1, 1),
        input_bindings=(0,),
        input_shapes=[(rows, cols)],
        output_shape=(rows, cols),
        rtol=rtol, atol=atol,
        grid=(rows, cols, 1),
        scalars=[dict(binding=2, dtype="u32", value=cols)],
        flops=rows * cols * flops_mul,
        bytes=2 * rows * cols * 4,
    )

